package org.iii.group3.transferdata.dto.ad;

public class PodcastAdDto {

	private String url;
	private String pictureString;
	private String clickCountUrl;
	private String text;
	
	public String getUrl() {
		return url;
	}
	public String getPictureString() {
		return pictureString;
	}
	public String getClickCountUrl() {
		return clickCountUrl;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	public void setPictureString(String pictureString) {
		this.pictureString = pictureString;
	}
	public void setClickCountUrl(String clickCountUrl) {
		this.clickCountUrl = clickCountUrl;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
	

	
}
